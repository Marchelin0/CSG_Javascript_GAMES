  draw(ctx) {
    for (let row = 0; row < this.map.length; row++) {
      for (let column = 0; column < this.map[row].length; column++) {
        let tile = this.map[row][column];
        if (tile === 1) { // als de waarde in het raster 1 is dan moet je de functie #drawWall aanroepen en dus de wall tekenen
          this.#drawWall(ctx, column, row, this.tileSize);
        } else if (tile === 0) { //als de waarde in het raster 0 is dan moet je de functie #drawDot aanroepen.
          this.#drawBurgir(ctx, column, row, this.tileSize);
        }

        ctx.strokeStyle = "yellow"; //het highliten van de buitenkant van de borders van de cellen, gebruiken bij het coderen.
        ctx.strokeRect(
          column * this.tileSize,
          row * this.tileSize,
          this.tileSize,
          this.tileSize
        );
      }
    }
  }
  #drawBurgir(ctx, column, row, size) {
    ctx.drawImage( //tekenen van het plaatje 
      this.burgir,
      column * this.tileSize,
      row * this.tileSize,
      size,
      size
    );
  }

  #drawWall(ctx, column, row, size) {
    ctx.drawImage(
      this.wall,
      column * this.tileSize,
      row * this.tileSize,
      size,
      size
    );
  }
  getPacman(velocity) {
    //waar is pacman, herhaling van hetzelfde principe eerder gebruikt
    for (let row = 0; row < this.map.length; row++) {
      for (let column = 0; column < this.map[row].length; column++) {
        let tile = this.map[row][column]; //de plaats van pacman
        if (tile === 4) {
          this.map[row][column] = 0; //ook een dot laten zien op de plek waar pacman zelf word neergezet
          return new Pacman(
            column * this.tileSize,
            row * this.tileSize,
            this.tileSize,
            velocity,
            this
          ); // binnen de haakjes staan de coordinateen, na komma hoe groot is de cell
        }
      }
    }
  }

  setCanvasSize(canvas) {
    canvas.width = this.map[0].length * this.tileSize;
    canvas.height = this.map.length * this.tileSize;
  }
